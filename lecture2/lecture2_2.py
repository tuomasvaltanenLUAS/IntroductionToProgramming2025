# adding new lines within text
print("Some text here!\nThis goes to another line!\n\n")

# empty print() also adds a single \n by default
# sometimes \n is not enough, you might need also to add \r (carriage return}
print("Something else...")
print()

# tabs can be used to align pieces of text horizontally
print("Total:\t\t350 €")
print("Tax:\t\t120 €")
print("Postage:\t20 €")