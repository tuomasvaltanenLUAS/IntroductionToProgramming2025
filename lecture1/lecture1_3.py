# summing up values
total = 176 + 287
print(total)

# substraction example
difference = 1856 - 917
print(difference)

# we typically use variables in calculations instead of
# just number values
salary = 2650

# basically => change salary to be
# old salary * 1.2
salary = salary * 1.2
print(salary)

# typical division
wind_speed = 25 / 3.6
print(wind_speed)

# you can use // to remove the decimals
apples = 25 // 4
print(apples)

# remainder (modulus) is the opposite of this -> %
# because 4 * 6 = 24 and 25 - 24 => 1
leftover = 25 % 4
print(leftover)
